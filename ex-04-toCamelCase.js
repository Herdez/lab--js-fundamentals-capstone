
/**
 * toCamelCase()
 *
 * Write a `toCamelCase` function that takes a multi word string as an argument
 * and returns a string that follows camel case convention
 *
 */

function toCamelCase(str) {
	var listOfWords = str.split(" ");
	for (var i = 1; i < listOfWords.length; i++) {
		var word = listOfWords[i].toLowerCase();
		listOfWords[i] = word.replace(word[0], word[0].toUpperCase());
	}
	return listOfWords.join("");
}



